local hoverInsert = false

local hoverCoords = {}
-----------------------------------------------------------------------------------------------------------------------------------------
-- THREADOPEN
-----------------------------------------------------------------------------------------------------------------------------------------
Citizen.CreateThread(function()
	while true do
		local timeDistance = 500
		local ped = PlayerPedId()
		if not IsPedInAnyVehicle(ped) then
			local coords = GetEntityCoords(ped)
			if not hoverInsert then
				for k,v in pairs(localCoords) do
					local distance = #(coords - vector3(v[1],v[2],v[3]))
					if distance < v[4] then
						hoverInsert = true
						hoverCoords = { v[1],v[2],v[3],v[4] }
						SendNUIMessage({ show = true, key = v[5], title = v[6], legend = v[7] })
					end
				end
			else
				local distance = #(coords - vector3(hoverCoords[1],hoverCoords[2],hoverCoords[3]))
				if distance > hoverCoords[4] then
					timeDistance = 4
					hoverInsert = false
					SendNUIMessage({ show = false })
				end	
			end
		end
		Citizen.Wait(timeDistance)
	end
end)